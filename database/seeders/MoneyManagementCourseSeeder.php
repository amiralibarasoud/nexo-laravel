    public function run(): void
    {
        $category = Category::where('slug', 'business')->first()
            ?? Category::first();

        $course = Course::create([
            'category_id'      => $category?->id,
            'title'            => 'دوره مدیریت پول',
            'slug'             => 'money-management',
            'short_description'=> 'یاد بگیر چطور پول‌هایت را مدیریت کنی، ثروت بسازی و از دام فقر مالی خارج شوی.',
            'description'      => $this->getCourseDescription(),
            'instructor_name'  => 'استاد علی رضایی',
            'instructor_bio'   => 'متخصص مدیریت مالی شخصی با بیش از ۱۵ سال تجربه در حوزه سرمایه‌گذاری و آموزش مالی.',
            'price'            => 4500000,
            'discounted_price' => null,
            'has_text'         => true,
            'has_audio'        => true,
            'level'            => 'all',
            'status'           => 'published',
            'is_featured'      => true,
            'published_at'     => now(),
            'lessons_count'    => 16,
            'duration_minutes' => 480,
            'students_count'   => 0,
        ]);

        $chapters = $this->getChapters();

        foreach ($chapters as $chapterIndex => $chapter) {
            $section = CourseSection::create([
                'course_id'   => $course->id,
                'title'       => $chapter['title'],
                'description' => $chapter['description'],
                'sort_order'  => $chapterIndex + 1,
            ]);

            foreach ($chapter['lessons'] as $lessonIndex => $lesson) {
                Lesson::create([
                    'course_id'             => $course->id,
                    'section_id'            => $section->id,
                    'title'                 => $lesson['title'],
                    'description'           => $lesson['description'],
                    'sort_order'            => $lessonIndex + 1,
                    'text_content'          => $lesson['text'],
                    'audio_duration_seconds'=> $lesson['audio_seconds'],
                    'is_preview'            => ($chapterIndex === 0 && $lessonIndex === 0),
                    'is_published'          => true,
                ]);
            }
        }

        $this->command->info("✅ دوره 'مدیریت پول' با ۴ فصل و ۱۶ درس ایجاد شد.");
    }

